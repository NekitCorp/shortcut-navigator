export function withAllDomains(
    name: string,
    options: HostnameOptions,
): Record<Hostname, HostnameOptions> {
    const records: Record<Hostname, HostnameOptions> = {};

    for (const domain of DOMAINS) {
        records[`${name}.${domain}`] = options;
    }

    return records;
}

